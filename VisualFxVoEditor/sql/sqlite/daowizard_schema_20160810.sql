
CREATE TABLE  IF NOT EXISTS `tbp_sys_dao_methods_h` (
	`HIST_TSP`	TEXT NOT NULL,
	`PACKAGE_NAME`	TEXT NOT NULL,
	`CLASS_NAME`	TEXT NOT NULL,
	`METHOD_NAME`	TEXT NOT NULL,
	`RESULT_VO_CLASS`	TEXT DEFAULT NULL,
	`SQL_BODY`	TEXT,
	`METHOD_DESC`	TEXT,
	`DML_TYPE`	TEXT,
	`FST_REG_DT`	INTEGER DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY(HIST_TSP,PACKAGE_NAME,CLASS_NAME,METHOD_NAME)
);

