	`PACKAGE_NAME`	TEXT NOT NULL,
	`CLASS_NAME`	TEXT NOT NULL,
	`METHOD_NAME`	TEXT NOT NULL,
	`RESULT_VO_CLASS`	TEXT NOT NULL,
	`SQL_BODY`	TEXT,
	`METHOD_DESC`	TEXT,
	PRIMARY KEY(PACKAGE_NAME,CLASS_NAME,METHOD_NAME)
);
